    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         if (jComboBox1.getSelectedItem().toString() == "Select Service") {
            JOptionPane.showMessageDialog(rootPane, "Selcet a Service from given list");
            return;
        }
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Driver Name");
            return;
        }
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Car Number");
            return;
        }
        if (jComboBox2.getSelectedItem().toString() == "Select Capacity") {
            JOptionPane.showMessageDialog(rootPane, "Selcet a Capacity from given list");
            return;
        }
        else {
           try {
                Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
                try {
                    con = DriverManager.getConnection("jdbc:odbc:test", "root", "");
                    s = con.createStatement();
                    query = "INSERT INTO carregistration (TypeOfVehicle, DriverName, CarNo, SeatingCapacity) VALUES('" + jComboBox1.getSelectedItem().toString() + "','" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + jComboBox2.getSelectedItem().toString() + "')";
                    s.executeUpdate(query);
                } catch (SQLException ex) {
                    Logger.getLogger(RegisterCar.class.getName()).log(Level.SEVERE, null, ex);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(RegisterCar.class.getName()).log(Level.SEVERE, null, ex);
            }
            Car obj = new Car(jComboBox1.getSelectedItem().toString(), jTextField1.getText(), (jTextField2.getText()), Integer.parseInt(jComboBox2.getSelectedItem().toString()), carid);

            cars[carid] = obj;
            carid++;
            jTextField1.setText("");
            jTextField2.setText("");

            jPanel1.setVisible(false); 
        }
    }//GEN-LAST:event_jButton3ActionPerformed

