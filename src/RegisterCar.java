    public void writetofile(){
        
            System.out.println(seatcapacity);
            File file = new File("CabService.txt");
            try {
                PrintWriter out = new PrintWriter(new FileWriter(file,true));
                out.println("Type Of Vehicle"+typeofvehicle);
                out.println("Driver Name"+drivername);
                out.println("Car Number"+carnumber);
                out.println("Seat Capacity"+seatcapacity);
                out.println();
                out.flush();
                out.close();
            } catch (IOException ex) {
                Logger.getLogger(Car.class.getName()).log(Level.SEVERE, null, ex);
            }
    }   
